        <p>
            Le sport est très apprécié et pratiqué sur Erentia, de façon amateur comme professionnelle.
            Au vu des capacités extraordinaires des habitants de l'île, les sports pratiqués diffèrent de ceux du reste
            de la planète. <br>
            Pour la plupart, il ne s’agit que de variantes des sports que l’on connaît, avec des adaptations des règles
            et du matériel : basketball avec des paniers à 6m de haut, tennis aérien avec des filets en acier et
            interdiction de toucher la balle si l’on touche le sol, etc, etc…. <br>
            Il existe également 3 sports inventés sur l'île, créés pour être spectaculaires. Il s'agit du Wodhun, du
            Quinn et de l'Arker.
        </p><p>
            Le Wodhun est un cousin du Rugby qui se joue avec une balle ronde et élastique, aux rebonds imprévisibles,
            le Dhun. Il existe une ligue de joueurs avec des capacités physiques augmentées et une autre ligue pour les
            joueurs à capacités physiques humaines qui jouent avec des équipements magiques augmentant leurs capacités.
            Il n’y a pas de matchs officiels entre les deux ligues mais des matchs amicaux sont fréquents. L'utilisation
            de la magie est assez limitée : seule la magie pour augmenter ses propres capacités physique est acceptée,
            et toute métamorphose ne peut pas durer plus d'un quart-temps. <br>
            Le Woofdun se joue en 4 quart-temps de 15 minutes par équipes de 10 joueurs. C’est un sport avec des règles
            simples, et peu dangereux s’il est bien encadré. C’est également le sport national de l’île.
        </p><p>
            Le Quinn se joue sous l’eau, dans des aquariums géants, par équipes de 8 joueurs, avec une balle en forme de
            fusée, le Fin. <br>
            Il y a 5 cerceaux au-dessus du terrain, placés plus ou moins loin au-dessus de la surface, dans lesquels les
            deux équipes essaient de lancer la balle pour marquer des points sans sortir de l’eau eux-mêmes (aucune
            partie de leur corps ne peut sortir de l'eau tant qu'ils sont en possession du Fin. Les cerceaux les plus
            hauts valent le plus de points. <br>
            L’intérêt majeur du Quinn réside cependant dans le fait que des balles en forme de poisson sont lâchées
            toutes les minutes dans le terrain et qu’elles sont enchantées pour pourrir la vie des joueurs. La majeur
            partie de l’équipe a donc pour mission de se débarrasser des balles-poissons _ appelées “shark” _  ou de les
            envoyer sur l’équipe adverse. Pour les joueurs qui ne sont pas capables de respirer sous l'eau, des charmes
            leurs sont fournis pour leur permettre de jouer à armes égales avec les autres. <br>
            L'utilisation de la magie est beaucoup plus libre que pour le Wodhun, mais les attaques contre les joueurs
            de l'éqiupe adverse (physiques, magiques, ou tentatives de lecture de pensée et contrôle mental) est
            strictement interdite. Il est également interdit d'invoquer des Fins/sharks supplémentaires, ou des
            créatures plus grosses qu'un dauphin qui gêneraient le déplacement des joueurs.
        </p><p>
            L'Arken est un cousin de l'archery tag, qui se joue aussi avec des arcs en plastique et des flèches à
            embouts en mousse. Néanmoins, les arcs sont renforcés par magie, le sport se déroule uniquement en extérieur
            dans un terrain très encombré d'environ 30m de long pour 10 de large, et les joueurs sont libres de se
            déplacer sur toute la surface du terrain. <br>
            Chaque équipe possède une base (3 murs et un toit) qui contient une cible. Une équipe gagne quand elle
            détruit la cible adverse ou capture tous les membres de l'équipe en les touchant avec les flèches en mousse.
            Les membres de l'équipe capturée peuvent être libérés comme dans le jeu des gendarmes et des voleurs, sauf
            s'ils sont physiquement incapables de reprendre la partie. <br>
            C’est un jeu inventé par l’armée de l’île pour servir d’entraînement aux recrues ou pour maintenir en forme
            les réservistes, et c’est le plus dangereux des jeux de l’île. <br>
            Toute forme de magie qui respecte les règles est autorisée, sauf celle qui vise à tuer et blesser gravement.
            Les secours sont toujours présents sur place lors des matchs officiels car les accidents sont fréquents.
            Comme c'est aussi le plus spectaculaire des jeux de l'île, c'est le plus populaire avec le Wodhun. Il en
            existe des variations de 5 à 20 joueurs par équipe, sur toutes les durées et dans toutes les conditions
            possibles.
        </p><p>
            Les combats d'animaux (et à plus forte raison d'alters) sont interdits sur l'île, ce qui en fait évidemment
            l'attraction la plus populaire d'Icnoyalt. Les arènes d'Icnoyalt sont les plus grandes de l'île, mais on en
            trouve de plus petites un peu partout sur l'île.
        </p>
