            Les 4 grandes villes étaient opposées et en concurrence mais aucune réelle guerre ouverte ne fut jamais
            déclarée, la protection de l’île dans sa totalité étant la priorité absolue de tous les habitants. <br>
            Au fil du temps, Itzel prit de plus en plus d’importance par rapport aux 3 autres villes, ayant sur son
            territoire la majorité des plaines agricoles de l’île et l’avantage de l’ancienneté. <br>

            Pour protéger l'île des Witch Hunters, de grandes tours furent construites tout autour de l'île, et des
            Derwenn furent charger de se relayer en permanence pour maintenir une immense barrière magique pour protéger
            l'île et la rendre invisible. Ces Derwenn venaient des 4 grandes villes sans distinction, et furent le
