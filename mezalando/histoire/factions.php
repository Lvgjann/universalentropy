        <div class="title">
            <span class="byline">Les Witch Hunters</span></div>
        <p>
        Les Witch Hunters sont une société secrète composée d’humains et dont l’unique but est l’éradication totale des
        Alters pour protéger l’Humanité.
        </p><p>
        On ne peut pas complètement leur en vouloir, quand on sait que nombre d'Alters utilisent les Nerics comme
        nourriture, sources d'ingrédients pour certains rituels, ou encore pour augmenter leur nombre.
        </p><p>
        Les Witch Hunters considèrent les mutants, non pas comme des Alters, mais comme les sauveurs de l'Humanité. Ils
        essaient donc d'en recruter par tous les moyens, et en comptent un nombre inconnu dans leurs rangs.
        </p><p>
        Les Alfren, une race ancienne de Mezalando, gardiens de savoirs millénaires et des mines de cristaux Maen, ont
        été éradiqués par les WH au siècle dernier. Plus récemment, ils ont réussi à tuer une Valkyrie, un être quasi
        divin et censé être immortel.
        </p>
        <div class="title">
            <span class="byline">La Diaspora Démons</span></div>
        <p>
        Les démons, qui sont techniquement des réfugiés climatiques, sont nombreux à Nenien, et ont tendance à vivre
        entre eux, pour tenter de préserver leur culture d'origine autant que possible.
        Ils sont alliés avec les autres Alters, mais restent assez réservés, presque repliés sur eux-mêmes pour certains
        groupes.
        De ce fait, il y a une forme de méfiance des autres Alters vis-à-vis des démons, et inversement. Ce n'est pas de
        l'hostilité, mais plutôt une forme d'incompréhension. Les autres race d'Alters sont si soudées entre elles,
        surtout dans les refuges cosmopolites comme Nenien, que la réserve relative des démons saute aux yeux.
        </p>
        <div class="title">
            <h2>Conflits</h2></div>
        <p>
            Même si tous les Alters ont un ennemi commun (les Witch Hunters), cela ne veut pas dire qu'ils sont tous
            unis : de nombreux aspects les divisent, et il existe différents groupes et organisations qui s'opposent les
            uns aux autres.
        </p>
        <div class="title">
            <span class="byline">Pénurie de cristaux Maen</span></div>
        <p>
            Les cristaux Maen sont la seule forme stable et fiable de stockage de Saàp pour les alters qui n'ont pas de
            réserves par eux-même, ou trop faibles, notamment la totalité des anges et des démons.
            Ils sont aussi nécessaires pour créer tout artefact magique, c'est à dire tout objet magique résistant au
            sel. Malheureusement, deux des plus grosses mines de cristaux Maen du monde sont tombées aux mains des Witch
            Hunters depuis la disparition des Alfren, ce qui fait que la demande dépasse maintenant l'offre.
        </p>
        <p>
            Les cristaux Maen sont devenus très rares, et donc très chers. Et tout ce qui est cher est convoité et
            trafiqué.
        </p>
        <p>
            Braquages, cambriolages, vol à la tire, tous les moyens sont devenus bons pour soustraire les précieux
            cristaux à leurs propriétaires et les revendre au prix fort. La "récupération" de cristaux est devenue une
            source de revenus non négligeable pour beaucoup d'organisations criminelles, des plus petits gangs aux plus
            grosses mafias.
            Deux groupes sont particulièrement connus dans ce domaine : EGP sur Erentia (Erentia Géologie les Pierres,
            oui c'est une blague par rapport à Europe écologie les Verts), et le MCC à Nenien (Maen Catalyst
            Collectors).
            En plus de récupérer par eux-même des cristaux de toutes les manières possibles, ils rachètent aussi des
            cristaux à d'autres groupes pour organiser la revente. Chacun dans leur zone géographique, ils sont la
            plaque tournante du traffic de cristaux Maen.
        </p>
        <div class="title">
            <span class="byline">Drogue</span></div>
        <p>
            Toutes les drogues humaines ne sont pas forcément efficaces sur les Alters, ou pas sur tous. Cependant, les
            Alters ont leurs propres drogues, et deux en particulier sont importantes : l'Or Vert et l'Ecarlate.
            La première est une drogue synthétique aux effets qui diffèrent selon les races, et peuvent parfois
            provoquer la mort subite chez les mutants. La seconde a au contraire le même effet chez toutes les race, qui
            s'apparente un peu à l'effet du cannabis.
            Les deux sont, pour le moment, trouvables uniquement sur l'île d'Erentia : l'Or Vert n'est synthétisé que
            par le groupe qui l'a découvert, et la recette n'a pas encore fuitée, tandis que l'Ecarlate est tirée d'une
            plante endémique de l'île.
        </p>
        <div class="title">
            <span class="byline">Technologie, Magie et traditions</span></div>
        <p>
            Si une grande partie des Alters voient la technologie comme un moyen des Nerics pour contrebalancer
            l'absence de Magie, et donc un accessoire pas forcément indispensable pour eux, d'autres ont un point de vue
            plus extrême. D'un côté, les technophiles cherchent à comprendre la technologie Neric, l'intégrer à la vie
            quotidienne des Alters, et même la fusionner avec la Magie, une voie nouvelle qui s'appelle la technomancie.
            De l'autre, les traditionnalistes sont au contraire fortement opposés à toute forme de technologie dépassant
            l'horlogerie. Lorsqu'ils possèdent de la technologie ou de l'électroménager, c'est par nécessité de passer
            inaperçu parmi les Nerics. De plus, les plus virulents des traditionnalistes défendent et continuent à
            utiliser certains rites et traditions tombés en désuétude, notamment tout ce qui implique des sacrifices
            humains (directement durant le rituel, ou pour obtenir des organes).
        </p>
        <p>
            Le groupe le plus extrême des traditionnalistes se nomme le Chêne Blanc. C'est un genre de société secrète
            qui, en plus d'être puriste à l'extrême, défend l'idée que les Alters sont supérieurs aux Nerics et
            devraient les gouverner. Ils pensent également que les Mutants sont des aberrations, des "sous-Alters".
        </p>
        <div class="title">
            <span class="byline">Crime organisé</span></div>
        <p>
            Les Alters n'échappent pas au crime organisé (ou non), et on trouve sur Mezalando des gangs ou organisations
            composés et/ou dirigés par des Alters. Les plus grandes organisations, qui ont des ramifications sur tous
            les continents et même sur l'île d'Erentia, touchent un peu à tout ce qui est illégal, mais ils ont chacun
            leur spécialité, suite à une série d'accords fragiles de non-agression pour le partage du territoire dans
            les Refuges. En effet, les Alters sont si peu nombreux que même les criminels hésitent à s'entre-tuer.

