        <div class="title">
            <h2>Drogues et alcool</h2>
        </div>
        <p>
            Un des problèmes des refuges d’Alters, et tout particulièrement Erentia qui regroupe toutes les races
            connues, est la législation concernant les drogues. Chaque race ne réagit en effet pas de la même façon aux
            mêmes substances. <br>
            Un terme légal spécifique à l’île est celui de vente contrôlée : un produit est accessible de façon légale
            aux particuliers, mais sous certaines conditions (en général être majeur et appartenir à une race
            particulière) et uniquement en faible quantité. En pratique, il existe des cartes pour chaque produit en
            vente contrôlée, à faire remplir à chaque achat par le revendeur. Une limite maximale de quantité est fixée
            (en général par mois). Ceux qui achètent systématiquement la quantité limite tous les mois s’exposent à une
            visite de la garde à leur domicile, où ils devront justifier que ce qu’ils ont acheté est bien destiné à
            leur consommation personnelle. <br>
            Bien entendu, tout cela ne compose que la face légale de l’iceberg, et de nombreux moyens de contournements
            existent pour se procurer à peu près n’importe quoi. Cependant, Erentia est une île aux frontières
            particulièrement contrôlée, il est donc plus difficile que dans d’autres pays de faire entrer et sortir des
            substances illicites de l’île. Dans les faits, cela se remarque au niveau du prix des drogues importées, qui
            est exorbitant. Cela explique en partie le succès des psychotropes “locaux” qui peuvent être produits
            directement sur l’île. <br>
            Ci-dessous sont listés quelques-unes des substances les plus connues et/ou les plus répandues sur l’île. Le
            terme “races humaines” regroupe les humains, mutants, derwenn et einherjärs, qui sont physiologiquement très
            proches les uns des autres, et réagissent de la même façon aux mêmes produits.
        </p>
        <div class="title">
            <span class="byline">Sucre</span></div>
        <p>
            <b>Actif sur toutes les races <br>
                Autorisé pour toutes les races</b>
        </p>
        <p>
            Légalement pas considéré comme une drogue, il est néanmoins important de noter que toutes les races
            absorbent et utilisent le sucre de la même façon. Le goût du sucre est inné chez toutes les races, y compris
            les races démoniaques et angéliques.
        </p>
        <div class="title">
            <span class="byline">Caféine, théine</span></div>
        <p>
            <b>Actif sur toutes les races sauf les Loenn <b></b>
                Autorisé pour toutes les races</b>
        </p>
        <p>
            Les produits contenants de la caféine et/ou de la théine sont considérés comme le sucre, c’est à dire qu’ils
            sont parfaitement légaux. Il faut noter cependant que ces deux substances sont totalement inefficaces sur
            les Loenn, qui préfèrent donc se booster au sucre.
        </p>
        <div class="title">
            <span class="byline">Alcool</span></div>
        <p>
            <b>Actif sur toutes les races, plus toxique chez les races non humaines <br>
                Vente contrôlée chez les majeurs</b>
        </p>
        <p>
            Si l’alcool est déjà très toxique pour les humains, les races non humaines y sont encore plus sensibles.
            Elles sont ivres plus vite, plus longtemps, et ont plus rapidement des dommages irréversibles liés à
            l’alcool. <br>
            Même si techniquement les Valkyries et Einherjärs ne peuvent pas mourir, et malgré les progrès de la
            médecine traditionnelle et magique, Erentia livre une véritable guerre contre l’alcool.
            On n’en trouve donc pas en grande surface, il faut aller l’acheter à des revendeurs de liqueurs agréés. La
            vente est autorisée à toutes les races, pourvu qu’on soit majeur, mais les quantités sont limitée. Bien
            évidemment, la législation sur l’ivresse au volant est ultra-stricte, et les campagnes de prévention contre
            l’alcool sont légions. <br>
            Cependant, l’île est surtout habitée par des races humaines, avec un lien fort et historique avec l’alcool.
            En pratique, les limites de quantités sont plutôt larges ( 75cL d’alcool >20°, 150cL entre 10 et 20°, et 3L
            d’alcool <10° par semaine). De plus, l’alcool consommé dans les bars et boîtes de nuit n’est pas décompté.
            Il s’agit plus d’empêcher une personne ou un groupe de personne d’acheter des quantités astronomiques
            d’alcool pour une soirée privée, où personne n’ira contrôler que tous les invités sont majeurs et où il n’y
            a aucune sécurité en cas d’accident.
        </p>
        <div class="title">
            <span class="byline">Tabac</span></div>
        <p>
            <b>Uniquement races humaines <br>
                Vente contrôlée chez les majeurs</b>
        </p>
        <p>
            Si techniquement la cigarette est toxique pour toutes les races, seules les races humaines sont sensibles à
            la nicotine. Il n’y a donc pas vraiment de problèmes de santé publique lié au tabac chez les races non
            humaines. <br>
            La législation sur l’île concernant le tabac est à peu près la même qu’en France. Cependant, le fait que les
            maladies liées au tabagisme soient traitables par la magie de soin fait que les mesures de l’état sont assez
            peu efficaces. La définition d’un lieu publique étant plus floue qu’en France, beaucoup de bars et de clubs
            ont réussi à contourner l’interdiction. Le paquet neutre a été un prétexte pour vendre des étuis décorés à
            mettre autour des paquets pour les personnaliser comme des coques de smartphones. Le tabac est en vente
            contrôlée dans des enseignes agréées, mais la limite par mois est très large et toutes les races y ont
            accès. En fait, la seule chose qui pousse vraiment les citoyens d’Erentia à arrêter de fumer, c’est le prix
            du paquet, très dissuasif. Le traffic de cigarettes est donc en fait le plus important de l’île, et de loin.
            Une différence notable entre la législation d’Erentia et celle de la france est qu’il est interdit de fumer
            au volant sur l’île (enfant dans la voiture ou pas).
        </p>
        <div class="title">
            <span class="byline">Cannabinoïdes</span></div>
        <p>
            <b>Uniquement races humaines <br>
                Cannabis autorisé sur ordonnance pour les races humaines, les feuilles de cannabis sont autorisées (en
                vente contrôlée) pour les derwenn.</b>
        </p>
        <p>
            Seuls les races humaines sont sensibles aux effets psychotropes des cannabinoïdes. Le cannabis est considéré
            comme un médicament psychotrope sur Erentia, et est donc soumis aux mêmes limitations que les autres
            médicaments de la classe. <br>
            Comme il est impliqué dans certaines préparations ou rituels derwenn, les derwenn majeurs y ont accès sans
            ordonnance en vente contrôlée. La quantité autorisée est cependant très faible, et seul le cannabis en
            feuilles leur est autorisé.
        </p>
        <div class="title">
            <span class="byline">Benzodiazépines (anxiolytiques)</span></div>
        <p>
            <b>Uniquement races humaines <br>
                Autorisé sur ordonnance pour les races humaines</b><br>
            Là encore, seuls les races humaines y sont sensibles (il existe des équivalents pour les autres races,
            distincts pour chaque race, mais qui suivent le même schéma en termes de législation). Ce sont des
            médicaments psychotropes qui ne sont accessibles que sur ordonnance nominative non renouvelable à durée
            limitée
        </p>
        <div class="title">
            <span class="byline">MDMA et autres amphétamines</span></div>
        <p>
            <b>Actif sur toutes les races <br>
                Interdits dans tous les cas</b><br>
            Ce sont dans tous les cas des stupéfiants illégaux, sans exception. Auparavant certains étaient catégorisés
            comme médicament psychotropes, on les retrouve donc plus facilement que les autres en circulation sur l’île.
        </p>
        <div class="title">
            <span class="byline">Cocaïne</span></div>
        <p>
            <b>Actif sur toutes les races <br>
                Interdit, sauf les feuilles de coca (vente contrôlée pour les derwenn)</b><br>
            La cocaïne est parfaitement illégale sur l’île, cependant les feuilles de coca sont en vente contrôlée pour
            les derwenn majeurs, dans le même contexte que pour les feuilles de cannabis. Ce sont d’ailleurs souvent les
            mêmes revendeurs pour les deux.
        </p>
        <div class="title">
            <span class="byline">HéroÏne et autres opidoïdes</span></div>
        <p>
            <b>Actifs sur toutes les races, mais beaucoup plus faiblement sur les races non humaines. br
                Interdits pour la plupart. Morphine et dérivée autorisés sur ordonnance. Opium et graines de pavot
                somnifères en vente contrôlée pour les Derwenn, les valkyries, les Einherjärs et les démons.</b>
        </p>
        <p>
            L’opium et tous ses dérivés sont actifs sur toutes les races, mais les races humaines y sont beaucoup plus
            sensibles. La morphine et ses dérivés sont autorisés sur ordonnance dans le contexte médical, mais son usage
            tend à se raréfier chez les espèces non humaines, à cause de son efficacité amoindrie. Comme pour les
            feuilles de coca et le cannabis, l’opium et les graines de pavot somnifères interviennent dans de nombreux
            rituels derwenn et leur est accessible en vente contrôlée. Plus étonnant, il existe une plante aux
            propriétés similaires sur Celestùn, et encore une autre sur Ignïs : les valkyries, les einherjärs et les
            démons ont donc eux aussi des rituels magiques pour lesquels ils utilisent l’opium en remplacement de la
            plante de leur monde d’origine, la vente contrôlée leur est donc ouverte. <br>
            L’usage récréatif est interdit, mais les démons en sont particulièrement friands. Le  pavot à opium pouvant
            être cultivé sur l’île, le trafic de ses dérivés est intense, surtout dans l’ouest et le sud de l’île.
        </p>
        <div class="title">
            <span class="byline">Écarlate</span></div>
        <p>
            <b>Actif sur toutes les races, mais particulièrement sur les mutants <br>
                Illégal</b>
        </p>
        <p>
            L’écarlate est une drogue produite à partir d’une plante endémique de l’île, de la même famille que l’ortie,
            qu’on appelle ortie écarlate. Elle est efficace sur toutes les races qui peuvent pratiquer la magie, et son
            effet semble proportionnel à la capacité de l’utilisateur à manipuler le saàp. Les mutants y sont
            particulièrement sensibles. Ses effets ressemblent à ceux du cannabis, mais, pendant plusieurs minutes à
            plusieurs heures après la prise, le consommateur voit sa capacité à contrôler et à manipuler la magie
            diminuée. L’écarlate est peu toxique en lui-même, mais dangereux à cause de la perte de contrôle qu’il
            engendre. Il est interdit sur l’île depuis plus d’un siècle, mais son usage et sa fabrication sont très
            répandus.
        </p>
        <div class="title">
            <span class="byline">Or vert</span></div>
        <p>
            <b>Actif sur toutes les races mais avec des effets différents <br>
                Illégal</b>
        </p>
        <p>
            L’or vert est une autre drogue spécifique de l’île, sous forme de poudre, efficace sur toutes les races.
            C’est une drogue synthétique, assez récente, et dont le procédé de fabrication n’est pas encore bien connu à
            part par ses fabricants.  Les effets sont différents d’une race à l’autre :
        </p>
        <ul>
            <li>
                somnifère et anxiolytique chez les Loenn, et les Einherjärs
            </li>
            <li>
                antidouleur et anxiolytique chez les anges et les valkyries
            </li>
            <li>
                excitant chez les derwenn (vigilance et sens exacerbés)
            </li>
            <li>
                hallucinogène, entactogène et/ou  euphorisant chez les démons
            </li>
            <li>
                effets très variables d’un mutant à l’autre, y compris des réactions extrêmes comme l’absence d’effet,
                l’épilepsie, le coma et même la mort subite
            </li>
        </ul>
        <p>
            Considérée comme extrêmement dangereuse, elle a un statut prioritaire dans la lutte contre la drogue. La
            recherche de son procédé de fabrication est également une priorité pour tous les groupes criminels qui
            voudraient faire tomber le monopole de la seule mafia restante actuellement en possession de la recette,
            basée à Icnoyalt.
        </p>
        <div class="title">
            <span class="byline">Autres cas notables</span></div>
        <p>
            Le chocolat est toxique pour les Loenn. Il ne fait pas l’objet d’une vente contrôlée, mais les produits
            contenants du chocolat doivent contenir une indication claire sur le paquet. Pour les biscuits et autres
            confiseries, on trouve dans le commerce des alternatives sans chocolat à presque tous les produits. <br>
            L’aspirine est extrêmement toxique pour toutes les races non humaines. Les conditions sont les mêmes que
            pour le chocolat : les médicaments qui contiennent de l’aspirine doivent l’indiquer clairement sur le
            paquet.
        </p>

