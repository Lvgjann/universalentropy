        <div class="title">
            <h2>La magie</h2>
            <span class="byline">Concept de base</span></div>
        <p>La Magie est un terme désignant tous les phénomènes qui ne peuvent pas être expliqués par les lois de la
            physique. Elle repose sur l’utilisation du saàp comme énergie pour forcer la création et le maintien de ces
            phénomènes non naturels. <br>
            Le saàp est invisible et intangible, mais son absence ou sa présence en quantités anormalement élevées sont
            sensibles, même par les humains sans pouvoirs. <br>
            Dans la vie de tous les jours, les différents effets Magiques existants sont séparés en grandes catégories,
            appelées [[Voies et Branches]].
        </p>
        <div class="title">
            <span class="byline">Saàp</span></div>
        <p>
            Le saàp est l’énergie universelle qui est consommée lors de la pratique de la Magie pour obtenir les effets
            surnaturels. Sur Mezalando, il n’existe qu’en quantités très faibles dans la nature. Il est produit par
            l’activité tectonique de la Terre, et absorbé en permanence de façon passive par les êtres vivants.
            Le saàp a tendance à circuler sous forme de lignes d'énergies, des "rivières" invisibles qui parcourent la
            surface du globe. Lorsque plusieurs lignes se croisent, elles forment un "nœud" d'énergie, une zone où la
            saàp est plus abondant qu'ailleurs, allant jusqu'à provoquer des phénomènes magiques spontanément.
            Erentia, est située sur un tel nœud, et un particulièrement important. La plupart des Refuges sont situés
            sur ligne d'énergie importante, ou au croisement de quelques petites. <br>
            Tous les êtres vivants originaires de Mezalando ont des stocks personnels de saàp, les alters ayant des
            stocks plus importants que les autres. <br>
            Sur Celestùn, le saàp est extrêmement abondant à l’état naturel car il est produit en permanence par les
            dieux, les dragons et les valkyries. Sur Ignïs, le saàp était une ressource fossile, très abondante à
            l’époque mais qui a été entièrement consommée. <br>
            C’est à cause de cette différence que les anges et les démons sont incapables d’utiliser la Magie sur
            Mezalando : ils sont incapables de stocker le saàp, et leur magie consomme de grandes quantités d’énergie.
            Les Valkyries, qui produisent leur propre saàp, ne sont pas concernées par cette limitation.
        </p>
        <div class="title">
            <span class="byline">Bracelets limiteurs et suppresseurs</span></div>
        <p>
            Les bracelets limiteurs et suppresseurs sont des objets que l'on trouve sur Erentia, rarement en-dehors, et
            qui permettent de limiter (resp. complètement supprimer) la capacité d'un Alter à pratiquer la Magie. Ils
            sont utilisés volontairement par les Alters qui contrôlent mal leurs pouvoirs, ou pour qui contrôler leurs
            pouvoirs est épuisant, ou comme moyen d'incapaciter un criminel. Ils fonctionnent sur tous les êtres
            vivants,
            peu importe l’espèce. <br>
            Les bracelets limiteurs diminuent le débit de saàp qui entre ou sort de son porteur : la magie est donc plus
            lente, moins explosive, et le temps de régénération du stock d’un individu est augmenté.
            Les bracelets suppresseurs drainent en permanence ( et en totalité ) le saàp du porteur, empêchant
            complètement l’utilisation de Magie. <br>
            Les bracelets, qu’ils soient limiteurs ou suppresseurs, sont des artefacts magiques. En tant que tels, ils
            peuvent donc être désenchantés pour les rendre inactifs. Logiquement, les bracelets suppresseurs ne peuvent
            être désenchantés par la personne qui les porte.
        </p>
        <div class="title">
            <span class="byline">Stockage du Saàp</span></div>
        <p>
            Le bois, les racines et écorces séchées, les os, poils et cornes, les dents, les sabots et les peaux
            d'animaux, ainsi que les métaux et cristaux sont capables de stocker de petites quantités de saàp. <br>

            Un cristal se dégage cependant du lot en étant capable de stocker de grandes quantités de saàp, et surtout
            de se recharger au fil du temps : le maen. Ce cristal permet de créer des catalyseurs maen, qui sont en
            fait des réserves d’énergie magique externes. Les anges, les démons, et les alters qui ont une réserve
            personnelle de saàp particulièrement faible sont les premiers clients de ces catalyseurs. <br>
            Lorsqu’il est déchargé en saàp, le maen est opaque. Il se recharge passivement avec le temps, comme les
            êtres vivants, devenant translucide en commençant par une extrémité. <br>
            Les mines de maen étaient historiquement exploitées par les Alfren, mais ils ont transmis leur savoir aux
            autres alters avant leur disparition complète. Les mineurs et gardiens des mines sont des golems créés par
            les alfren et alimentés par d’énormes cristaux maen eux-mêmes. <br>
            Durant les 5 dernières années, les Witch Hunters ont pris le contrôle de 2 des mines de maen les plus
            importantes. Il y a maintenant une quasi-pénurie de maen, et un trafic s’est mis en place pour voler des
            catalyseurs et les revendre à prix d’or au marché noir. <br>
            Le maen peut également être créé de façon synthétique, via une technologie volée aux Witch Hunters. Ces
            cristaux synthétiques sont incolores (blancs quand ils sont déchargés), ce qui les différencie des cristaux
            naturels. On ne peut en fabriquer que de petite taille, ils ne peuvent donc jamais stocker de très grandes
            quantités de saàp. <br>
            Tous les objets magiques en métal, résistants à l’eau salée, contiennent des cristaux maen. Les plus petits
            et les moins chers ne contiennent que de la poudre ou des éclats issus de la taille de cristaux plus gros.
        </p>
        <p>
            La dernière façon de stocker le saàp est sous forme liquide. Il s’agit alors d’un liquide doré, épais, qui
            doit être contenu dans des gélules contenant une forte proportion de particules d'or et de platine qu'on
            appelle "capsules". <br>
            Ce saàp extrêmement concentré permet d’obtenir un incroyable gain de puissance magique pendant quelques
            minutes, mais est à usage unique et extrêmement cher. Certains rituels magiques, notamment chez les anges et
            les démons, nécessitent tellement de saàp qu’il est impossible de les réaliser sans ces capsules.
            L'utilisation des capsules est dangereuse pour les personnes qui ne sont pas habituées à une telle quantité
            de saàp à la fois, et peut provoquer un état qu'on appelle "l'ivresse du saàp". Les victimes de l'ivresse du
            saàp perdent leurs capacités de jugement (comme si elles étaient ivres), pouvant devenir violentes,
            euphoriques, ou au contraire sombrer dans le désespoir, et la magie se manifeste de façon imprévisible et
            incontrôlée autour d'eux.
        </p>
        <div class="title">
            <span class="byline">La limite entre les Nerics et les Alters</span></div>
        <p>
            La limite entre les Alters, qui peuvent utiliser la Magie, et les Nerics, qui ne peuvent pas, est beaucoup
            plus floue qu'on l'imagine au premier abord. <br>
            C'est particulièrement vrai pour les humains. Même parmi les humains "normaux", il en existe qui ont des
            réserves de saàp plus importantes que la moyenne, ou qui ont un faible don de divination, avec par exemple
            des rêves prémonitoires ou des intuitions anormalement précises. Au contraire, il naît régulièrement dans
            des familles de Derwenn des membres ayant une réserve personnelle de saàp trop faible pour pratiquer la
            magie sans un cristal Maen. <br>
            La différence se fait dans ces cas-là par rapport à la connaissance de l'existence des Alters, à l'étude ou
            non de la magie, et plus généralement à la capacité de contrôler les effets magiques, aussi faibles
            soient-ils. <br>
            Les mutants eux, ont en commun d'avoir une réserve personnelle de saàp particulièrement importante, au point
            que des effets magiques se manifestent spontanément, parfois de façon spectaculaire.
        </p>
        <div class="title">
            <span class="byline">La magie et le sel</span></div>
        <p>
            Le sel, plus particulièrement dissout dans de l'eau, possède la propriété de libérer dans l'atmosphère le
            saàp stocké dans les matières organiques. En pratique, le sel et l'eau salée dissipent les effets des
            potions et des charmes, annulent les effets des sortilèges, et drainent le saàp des êtres vivants. <br>

            L'effet est presque imperceptible chez les Alters eux-mêmes, vu leurs réserves importantes de Saàp.
            Cependant, quand ils mangent des plats très salés, par exemple de la morue ou des nouilles instantanées, ils
            peuvent se retrouver incapables de faire de la magie pendant quelques dizaines de minutes à quelques heures.
            <br>

            Les cristaux et métaux, qui emprisonnent mieux le saàp, sont insensibles au sel, ce qui explique que les
            cristaux maen et les capsules de saàp concentré ne rencontrent pas de problème. <br>

            Les mers et océans sont suffisamment salés pour que les Alters ne puissent pas utiliser la magie quand ils
            se baignent dedans (ce qui ne les empêche en rien de piquer une tête soyons clairs), mais les embruns ou
            autres faibles quantités d'eau salée sur la peau ne sont pas assez forts pour les entraver. <br>
            Les embruns sont par contre une plaie pour les charmes en bois, les potions, et toute forme de magie qui
            utilise des restes animaux et végétaux (divination par exemple). <br>

            C'est aussi l'eau salée des océans qui empêche la faune et la flore magique de l'île d'en sortir et de
            coloniser le reste de la planète. <br>

            Il existe un peu partout dans le monde des croyances sur les propriétés purificatrices du sel : faire des
            cercles de sel pour éloigner le mauvais sort, répandre du sel sur le pas de la porte et les encadrements de
            fenêtre pour se prémunir des mauvais esprits… <br>
            Ce sont les Alters eux-mêmes qui ont répandu ces légendes très exagérées sur le sel, dans le but de faire
            croire aux Witch Hunters que le sel était leur faiblesse ultime. En réalité, rien ne se passera si un Alter
            marche sur une ligne de sel faite au sol. <br>

            Par contre, il existe réellement des histoires comme quoi certains Alters mal intentionnés ont
            volontairement jeté du sel dans les marmites de leurs concurrents pour saboter leur production de charmes.
            L'expression "être salé", qui est maintenant passée dans le langage courant même chez les Neric, désigne au
            départ le dégoût de s'être fait avoir par ce genre d'actes malveillants.
        </p>
